							<b>Bikram yoga: </b> 
							<br>People practice Bikram yoga, also known as hot yoga, in artificially heated rooms at a temperature of nearly 105oF and 40% humidity. It consists of 26 poses and a sequence of two breathing exercises.
						    </li>

						    <li style="font-size: 20px;">
						    <b>Bhakti yoga: </b>
						    <br>This is a generic term for any type of yoga that teaches physical poses. Hatha classes usually serve as a gentle introduction to the basic poses of yoga.
